// 3. WAP to add, subtract, multiply, divide numbers. 
#include <iostream>
using namespace std;
int main()
{
    int num1,num2;
cout<<"Enter the first number: ";
cin>>num1;
cout<<"Enter the second number: ";
cin>>num2;

cout<<"Addition of "<<num1<<"+"<<num2<<"="<<num1+num2<<endl;
cout<<"Difference of "<<num1<<"-"<<num2<<"="<<num1-num2<<endl;
cout<<"Multiplication of "<<num1<<"*"<<num2<<"="<<num1*num2<<endl;
cout<<"division of "<<num1<<"/"<<num2<<"="<<num1/num2<<endl;

return 0;
}