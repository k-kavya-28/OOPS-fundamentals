#include <iostream>
using namespace std;
int main()
{
    float a, b, c, d, e, f;
    cout << "Input number 1: ";
    cin >> a;
    cout << "Input number 2: ";
    cin >> b;
    c = a + b;
    cout << c << " is the sum" << endl;
    d = a - b;
    cout << d << " is the difference" << endl;
    e = a * b;
    cout << e << " is the product" << endl;
    f = a / b;
    cout << f << " is the result of division" << endl;
    return 0;
}
